MAIN	START	1000
	LDT	#1
FIRST	JSUB	STKINIT
			
RLOOP1	TD	INPUT	.Initialize Node Number
	JEQ	RLOOP1
	LDA	#0
	RD	INPUT
	COMP	ENTER
	JEQ	ARRINIT
	STA	NODE_NO
	J	RLOOP1
	
ARRINIT	LDX	#0	.Initialize Node Array
RLOOP2	LDA	#0
	TD	INPUT
	JEQ	RLOOP2
	RD	INPUT
	COMP	SPACE	.Ignore Space
	JEQ	RLOOP2
	STCH	Arr, X
	TIX	MAXLEN
	COMP	ENTER
	JEQ	LINECNT
	J	RLOOP2	

LINECNT	LDS	LINE
	ADDR	T,S
	STS	LINE
	LDA	#0
	LDA	LINE
	COMP	NODE_NO
	JEQ	EXIT
	J	RLOOP2

LINE	WORD	48
COM	WORD	97

.Stack
STKINIT	LDA	#STACK
	STA	TOP
	RSUB

PUSH	LDA	TOP
	ADD	#3
	STA	TOP
	RSUB

POP	LDA	TOP
	SUB	#3
	STA	TOP
	RSUB

STACK	RESW	30
TOP	RESW	1

INPUT	BYTE	0
OUTPUT	BYTE	1
ENTER	WORD	10
SPACE	WORD	32

NODE_NO	RESW	1
Arr	RESB	200
MAXLEN	WORD	200


EXIT	LDX	#0
